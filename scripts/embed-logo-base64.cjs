/**
 * Script to convert logo.png to base64 and embed in email templates
 * Run with: node scripts/embed-logo-base64.cjs
 */

const fs = require('fs');
const path = require('path');

const logoPath = path.join(__dirname, '..', 'public', 'logo.png');
const outputTxtPath = path.join(__dirname, '..', 'src', 'lib', 'logo-base64.txt');
const outputTsPath = path.join(__dirname, '..', 'src', 'lib', 'logo-base64.ts');

try {
  if (!fs.existsSync(logoPath)) {
    console.error('âŒ Logo file not found at:', logoPath);
    process.exit(1);
  }

  const imageBuffer = fs.readFileSync(logoPath);
  const base64 = imageBuffer.toString('base64');
  const dataUri = `data:image/png;base64,${base64}`;

  // Save to text file
  fs.writeFileSync(outputTxtPath, dataUri, 'utf8');

  // Save to TypeScript file for easier import
  const tsContent = `/**
 * Base64 encoded logo for embedding in email templates
 * This ensures the logo displays even when email clients block external images
 * 
 * Generated by: scripts/embed-logo-base64.cjs
 * To regenerate: node scripts/embed-logo-base64.cjs
 */

// Base64 data URI for logo.png
export const LOGO_BASE64_DATA_URI = '${dataUri}';

// For backwards compatibility, also export as a function that returns the data URI
export const getLogoBase64 = (): string => LOGO_BASE64_DATA_URI;
`;

  fs.writeFileSync(outputTsPath, tsContent, 'utf8');

  console.log('âœ… Logo converted to base64');
  console.log('ğŸ“ Base64 length:', base64.length, 'characters');
  console.log('ğŸ’¾ Saved to:', outputTxtPath);
  console.log('ğŸ’¾ Saved TypeScript file to:', outputTsPath);
  console.log('ğŸ“‹ Data URI prefix:', dataUri.substring(0, 50) + '...');
} catch (error) {
  console.error('âŒ Error converting logo:', error.message);
  process.exit(1);
}

